-(NSDictionary *)bookcase {
    if (_bookcase == nil) {
        NSMutableArray *temp = [NSMutableArray arrayWithCapacity:kShelvesCount];
        _bookcase = @{@"shelvesCount" : @(kShelvesCount),
                      @"width_cm"     : @55,
                      @"shelf ID#"    : @1,
                      @"shelves"      : temp
                      };
    }
    return _bookcase;
}

